/* Print this simple pattern without using any loops and only print statements

 * 
 **
 ***
 ****
 *****

 */

public class PatternPrint {
    public static void main(String[] args) {
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****");

        // OR

        System.out.println("\n");
        System.out.print("*\n**\n***\n****\n*****");

    }
}